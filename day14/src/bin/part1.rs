fn run(input: &str) -> u32 {
    let mut total_load: u32 = 0;

    let lines: Vec<&str> = input.lines().collect();
    let line_num = lines.len();
    let line_width = lines[0].len();

    let mut columns: Vec<String> = Vec::new();
    for i in 0..line_width {
        let mut col: String = String::from("");

        for j in 0..line_num {
            col += input
                    .lines()
                    .collect::<Vec<&str>>()[j]
                    .get(i..(i+1))
                    .unwrap();
        }

        columns.push(col);
    }

    let re_1 = Regex::new(r"#(?<a>\.+)O").unwrap();
    let re_2 = Regex::new(r"O(?<a>\.+)O").unwrap();
    let re_3 = Regex::new(r"^(?<a>\.+)O").unwrap();

    for mut col in columns {
        while re_1.is_match(col.as_str()) || re_2.is_match(col.as_str()) || re_3.is_match(col.as_str()) {
            col = re_1.replace(col.as_str(), "#O$a").to_string();
            col = re_2.replace(col.as_str(), "OO$a").to_string();
            col = re_3.replace(col.as_str(), "O$a").to_string();
        }

        let mut row_weight: u32 = line_num as u32;
        let mut load: u32 = 0;

        for c in col.chars().into_iter() {
            if c == 'O' {
                load += row_weight;
            }
            row_weight -= 1;
        }

        total_load += load;
    }

    return total_load
}

